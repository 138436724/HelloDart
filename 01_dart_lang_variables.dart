void var_name() {
  var name = 'Bob';
  print('var name is $name');
}

void Object_name() {
  Object name = 'Bob';
  print('Object name is $name');
}

void String_name() {
  String name = 'Bob';
  print('String name is $name');
}

void main() {
  var_name();
  Object_name();
  String_name();
}